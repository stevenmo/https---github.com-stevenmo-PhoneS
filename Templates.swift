
import Foundation

fileprivate func MYFONT(_ s: Float ) -> UIFont {
    return UIFont.systemFont(ofSize: CGFloat(s))
}

fileprivate func BOLDFONT(_ s: Float ) -> UIFont {
    return UIFont.boldSystemFont(ofSize: CGFloat(s))
}

fileprivate func LightFONT(_ s: Float ) -> UIFont {
    if #available(iOS 8.2, *) {
        return UIFont.systemFont(ofSize: CGFloat(s), weight: UIFontWeightLight )
    } else {
        return MYFONT(s)
    }
}

fileprivate func MYRECT(_ x: Float,_ y: Float,_ w: Float,_ h: Float ) -> CRect {
    
    return CRect.initWith(x: x, posY: y, width: w, height: h) as! CRect
}

fileprivate func MYSIZE(_ s: Float,_ v: UIView ) -> Float {
    
    return CRect.getSizeByDevice(s, parentView: v )
}

fileprivate func MYRECTR(_ r: CGRect ) -> CRect {
    
    return CRect.initWith(r) as! CRect
}

fileprivate func MYRECTI(_ x: Float,_ y: Float,_ w: Float,_ h: Float ) -> CRect {
    
    return CRect.initWithD(x, posY: y, width: w, height: h, bDeviceIndependent: true ) as! CRect
}

fileprivate func CENTER_RECT(_ x: Float,_ y: Float,_ w: Float,_ h: Float ) -> CRect {
    
    return CRect.initWithCenter(x, posY: y, width: w, height: h) as! CRect
}

fileprivate func CENTER_RECTI(_ x: Float,_ y: Float,_ w: Float,_ h: Float ) -> CRect {
    
    return CRect.initWithCenterI(x, posY: y, width: w, height: h) as! CRect
}

fileprivate func CENTER_RECTX(_ x: Float,_ y: Float,_ w: Float,_ h: Float ) -> CRect {
    
    return CRect.initWithCenterX(x, posY: y, width: w, height: h) as! CRect
}

fileprivate func MYINT(_ i: Float ) -> NSNumber {
    
    return NSNumber(value: i)
}

fileprivate func COLOR(_ a: Int, _ b: Int, _ c: Int ) -> UIColor {
    
    return UIColor(red: CGFloat(a)/CGFloat(255), green: CGFloat(b)/CGFloat(255), blue: CGFloat(c)/CGFloat(255), alpha: 1.0 )
}

fileprivate func COLOR_INT(_ a: Int ) -> UIColor {
    return UIColor(red: CGFloat(a>>16)/CGFloat(255), green: CGFloat((a>>8)&0xff)/CGFloat(255), blue: CGFloat(a&0xff)/CGFloat(255), alpha: 1.0 )
}

fileprivate func COLORF(_ a: Int, _ b: Int, _ c: Int, _ d: Float ) -> UIColor {
    
    return UIColor(red: CGFloat(a)/CGFloat(255), green: CGFloat(b)/CGFloat(255), blue: CGFloat(c)/CGFloat(255), alpha: CGFloat(d) )
}

